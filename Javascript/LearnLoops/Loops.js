/* Loops helps to do certain things repeatedly  */

/* For loops when u know where to stop exactly */
for(let i=0; i<=10;i++){
    console.log(i);
    
};

/* While when do'nt know where to stop but should execute until its true */
let j=0;
while(j<50){
    console.log(j);
    j++;
}
/* Either given condition true or false atleat execute the loop body once */
let k=50;
do{
    console.log(k);
    k++;
    
}while(k<60);